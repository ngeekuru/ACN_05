package com.tnsif.loops;

public class forloop {

	public static void main(String[] args) {
		for(int i=1;i<=10;i++)
		{
			System.out.print(i);
			
		}
		System.out.println("");
		
		
		int n=10;
		while(n<=20)
		{
			System.out.print(n+",");	
			n++;
		}
		System.out.println("");
		
		int j=1;
		do {
			System.out.println(j);
			j++;
		}while(j>=10); //1,2,
	}

}
